package es.onebox.flc.orders.dto.groups;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;

import java.io.Serial;
import java.io.Serializable;

public class AttributeValueInfoDTO implements Serializable {

    @Serial
    private static final long serialVersionUID = -3696279462639128613L;

    private Integer valueId;

    private Integer attributeId;

    private String name;

    private Boolean defaultAttribute;

    private String code;

    public Integer getValueId() {
        return valueId;
    }

    public void setValueId(Integer valueId) {
        this.valueId = valueId;
    }

    public Integer getAttributeId() {
        return attributeId;
    }

    public void setAttributeId(Integer attributeId) {
        this.attributeId = attributeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getDefaultAttribute() {
        return defaultAttribute;
    }

    public void setDefaultAttribute(Boolean defaultAttribute) {
        this.defaultAttribute = defaultAttribute;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Override
    public boolean equals(Object obj) {
        return EqualsBuilder.reflectionEquals(this, obj);
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

}
